#include "../includes/ft_printf.h"

void	print_pad(int zero, int nb)
{
	int		i;
	char	c;

	c = (zero) ? '0' : ' ';
	i = 0;
	while (i < nb)
	{
		ft_putchar(c);
		i++;
	}
}
